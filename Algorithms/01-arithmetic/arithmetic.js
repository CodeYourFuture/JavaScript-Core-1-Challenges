// Fill in the body of each function with the code required

var add = function(num1, num2) {
    /*
    simply returns the sum of
    parameter 'num1' and parameter 'num2'
    */
    return (num1 + num2);
};

var subtract = function(num1, num2) {
    /*
    simply returns the difference between
    parameter 'num1' and parameter 'num2'
    */
    return (num1 - num2);
};

var multiply = function(num1, num2) {
    /*
    simply multiplies parameter 'num1' 
    by parameter 'num2'
    */
    return (num1 * num2);
};

var divide = function(num1, num2) {
    /*
    simply divides parameter 'num1' 
    by parameter 'num2'
    */
    return(num1 / num2);
};
